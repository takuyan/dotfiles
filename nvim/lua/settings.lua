-- settings.lua: Neovim の基本設定

vim.opt.number = true             -- 行番号表示
-- vim.opt.relativenumber = true     -- 相対行番号
vim.opt.expandtab = true          -- タブをスペースに
vim.opt.shiftwidth = 2            -- インデント幅
vim.opt.tabstop = 2               -- タブ幅
vim.opt.cursorline = true         -- 現在行ハイライト
vim.opt.clipboard = "unnamed"     -- OSのクリップボードと共有
vim.opt.splitright = true         -- 垂直分割を右に
vim.opt.splitbelow = true         -- 水平分割を下に
